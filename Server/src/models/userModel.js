import { Schema, model } from "mongoose";

const userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        Enum: ["user", "auther"],
        default: "user"
    }

}, { timestamps: true })

export const userModel = model("User", userSchema)